# ---------------------------------------------------------------------------
# Colors
# ---------------------------------------------------------------------------
NAVY    = colors.HexColor("#1B2A4A")
BLUE    = colors.HexColor("#2E5090")
ACCENT  = colors.HexColor("#4A7CC7")
CARD_BG = colors.HexColor("#EEF2F8")
META_BG = colors.HexColor("#243860")
DIVIDER = colors.HexColor("#CBD5E8")
WHITE   = colors.white
TXT     = colors.HexColor("#1A1A1A")
GRAY    = colors.HexColor("#555555")
LIGHT   = colors.HexColor("#888888")

# ---------------------------------------------------------------------------
# Page geometry
# ---------------------------------------------------------------------------
PAGE_W, PAGE_H = A4
MARGIN   = 0.75 * inch
BOT_MAR  = 1.0  * inch
USABLE_W = PAGE_W - 2 * MARGIN   # ≈ 487 pts
HALF_W   = USABLE_W / 2


# ---------------------------------------------------------------------------
# Exception
# ---------------------------------------------------------------------------
class ReportGenerationError(Exception):
    pass


# ---------------------------------------------------------------------------
# Numbered Canvas  — Page X of Y footer on every page
# ---------------------------------------------------------------------------
class _NumberedCanvas(pdf_canvas.Canvas):
    def __init__(self, *args, **kwargs):
        pdf_canvas.Canvas.__init__(self, *args, **kwargs)
        self._pages: list[dict] = []

    def showPage(self):
        self._pages.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        total = len(self._pages)
        for i, state in enumerate(self._pages, 1):
            self.__dict__.update(state)
            self._stamp(i, total)
            pdf_canvas.Canvas.showPage(self)
        pdf_canvas.Canvas.save(self)

    def _stamp(self, page_num: int, total: int) -> None:
        self.saveState()
        self.setStrokeColor(DIVIDER)
        self.setLineWidth(0.5)
        self.line(MARGIN, 52, PAGE_W - MARGIN, 52)
        self.setFont("Helvetica", 8)
        self.setFillColor(LIGHT)
        self.drawCentredString(
            PAGE_W / 2, 36,
            f"Generated by AI Resume Analyzer   |   Page {page_num} of {total}"
        )
        self.restoreState()


