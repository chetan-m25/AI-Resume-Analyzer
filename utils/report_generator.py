def _section_bar(text: str) -> Table:
    tbl = Table(
        [[Paragraph(text.upper(), STYLES["sec_head"])]],
        colWidths=[USABLE_W],
    )
    tbl.setStyle(TableStyle([
        ("BACKGROUND",    (0,0),(-1,-1), NAVY),
        ("TOPPADDING",    (0,0),(-1,-1), 7),
        ("BOTTOMPADDING", (0,0),(-1,-1), 7),
        ("LEFTPADDING",   (0,0),(-1,-1), 12),
        ("RIGHTPADDING",  (0,0),(-1,-1), 12),
    ]))
    return tbl


def _bullets(items: list) -> ListFlowable:
    display = items if items else ["None detected."]
    return ListFlowable(
        [
            ListItem(
                Paragraph(str(item), STYLES["bullet"]),
                bulletColor=ACCENT,
                leftIndent=16,
                spaceAfter=3,
            )
            for item in display
        ],
        bulletType="bullet",
        bulletFontSize=8,
        leftIndent=20,
        spaceAfter=6,
    )


def _bullet_section(heading: str, items: list) -> KeepTogether:
    return KeepTogether([
        _section_bar(heading),
        Spacer(1, 0.1 * inch),
        _bullets(safe_list(items)),
        Spacer(1, 0.15 * inch),
    ])


def _summary_section(text: str) -> KeepTogether:
    return KeepTogether([
        _section_bar("AI Professional Summary"),
        Spacer(1, 0.1 * inch),
        Paragraph(text or "No summary available.", STYLES["body"]),
        Spacer(1, 0.15 * inch),
    ])


# ---------------------------------------------------------------------------
# Main renderer
# ---------------------------------------------------------------------------

def _render_pdf(result: dict, save_path: str, original_filename: str = "") -> None:
    doc = SimpleDocTemplate(
        save_path,
        pagesize=A4,
        leftMargin=MARGIN,
        rightMargin=MARGIN,
        topMargin=MARGIN,
        bottomMargin=BOT_MAR,
        title="AI Resume Analysis Report",
        author="AI Resume Analyzer",
    )

    story: list = []

    story.append(_header_block(original_filename))
    story.append(Spacer(1, 0.3 * inch))

    story.append(_score_cards(
        int(result.get("resume_score", 0)),
        int(result.get("ats_score",    0)),
    ))
    story.append(Spacer(1, 0.3 * inch))

    for heading, key in [
        ("Technical Skills", "technical_skills"),
        ("Soft Skills",      "soft_skills"),
        ("Strengths",        "strengths"),
        ("Missing Skills",   "missing_skills"),
        ("Suggestions",      "suggestions"),
        ("Weaknesses",       "weaknesses"),
    ]:
        story.append(_bullet_section(heading, result.get(key, [])))

    story.append(_summary_section(result.get("summary", "")))

    doc.build(story, canvasmaker=_NumberedCanvas)
